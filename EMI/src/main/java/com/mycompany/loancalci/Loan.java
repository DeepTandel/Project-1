    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String path="";
        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x = j.showSaveDialog(this);//jtable
        
        if (x==JFileChooser.APPROVE_OPTION){
            path = j.getSelectedFile().getPath();
        }
        Document doc = new Document();  
        
        try {
            PdfWriter.getInstance(doc, new FileOutputStream(path+"Emi.pdf"));
             doc.open();;
           
            PdfPTable tb = new PdfPTable(4);
            tb.addCell("Payment");
            tb.addCell("Interest");
            tb.addCell("Principal");
            tb.addCell("Balance");
  
            for(int k = 0;k< jTable1.getRowCount()-1;k++){
               
                String Payment = jTable1.getValueAt(k,0).toString();
                String Interest = jTable1.getValueAt(k,1).toString();
                String Principal = jTable1.getValueAt(k,2).toString();
                String Balance = jTable1.getValueAt(k,3).toString();
                
                tb.addCell(Payment);
                tb.addCell(Interest);
                tb.addCell(Principal);
                tb.addCell(Balance);
            }
            doc.add(tb);                    
                    } catch (FileNotFoundException ex) {
            Logger.getLogger(Loan.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(Loan.class.getName()).log(Level.SEVERE, null, ex);
        }
        doc.close();           
    }//GEN-LAST:event_jButton2ActionPerformed

